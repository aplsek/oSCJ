package ovm.services.io.async;

/**
 * Exactly the same thing as <code>struct linger</code> in <code>&lt;sys/socket.h^gt;</code>.
 * @author Filip Pizlo
 */
public class Linger {
    public boolean onoff;
    public int linger;
    
    public Linger() {
        this.onoff=false;
        this.linger=0;
    }
    
    public Linger(boolean onoff,
                  int linger) {
        this.onoff=onoff;
        this.linger=linger;
    }
}

